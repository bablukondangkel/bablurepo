package com;

public class IfElseTest {
	public static void main(String args[]) {
		int i=20;
		
	if(i==10) {
		System.out.println("value of i is=10");
	}
	else if(i==20) {
		System.out.println("value of i is=20");
	}
	else if(i==30) {
		System.out.println("value of i is=30");
	}
	else {
		System.out.println("This number is not here="+i);
	}
	}

}
