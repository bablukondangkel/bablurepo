package com.practice;

public class StarForLoop {
	public static void main(String[] arg) {
		for(int i=10;i>=1;i--) {
			for(int j=10;j>=i;j--) {
				System.out.print(" *");
			}
			System.out.println();
		}
		for(int i=10;i>=1;i--) {
			for(int j=10;j>=i;j--) {
				System.out.print(" *");
	}
			System.out.println();

}
		
}
}