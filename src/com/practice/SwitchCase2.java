package com.practice;

public class SwitchCase2 {
	public static void main(String[] args) {
		String name="yogesh";
		switch(name) {
		case "Bablu":
			System.out.println("Bablu is a man");
			break;
		case "Babu":
			System.out.println(" Babu is a Java hero ");
			break;
		case "yogesh":
			System.out.println("yogesh is a java hero"); 
			break;
		default:	
			System.out.println();
}
	}

}
